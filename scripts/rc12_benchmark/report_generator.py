"""
RC12 REPORT GENERATOR — Markdown + JSON Benchmark Reports

Generates structured, readable reports from benchmark aggregate data.
"""

from typing import Optional
import time


class ReportGenerator:
    """Generates benchmark reports in multiple formats."""

    def generate_markdown(self, aggregate: dict,
                          results: list = None) -> str:
        """
        Generate a full Markdown benchmark report from aggregate data.
        """
        total = aggregate.get('total', 0)
        if total == 0:
            return "# Benchmark Report\n\n**No cases executed.**"

        lines = [
            f"# CT RC12 Benchmark Report",
            f"**Generated:** {time.strftime('%Y-%m-%d %H:%M:%S')}",
            f"**Dataset:** {aggregate.get('dataset', 'unknown')}",
            f"**Engine:** {aggregate.get('engine', 'auto')}",
            f"**Cases:** {total}",
            f"**Duration:** {aggregate.get('duration_ms', 0):.0f}ms",
            "",
            "---",
            "",
            "## 📊 Executive Summary",
            "",
            self._summary_table(aggregate),
            "",
            "---",
            "",
            "## 🔧 Repair Metrics",
            "",
            self._repair_section(aggregate),
            "",
            "---",
            "",
            "## 🛡️ Security Metrics",
            "",
            self._security_section(aggregate),
            "",
            "---",
            "",
            "## ⚡ Performance",
            "",
            self._performance_section(aggregate),
            "",
            "---",
            "",
            "## 🧠 Learning",
            "",
            self._learning_section(aggregate),
            "",
            "---",
            "",
            "*Generated by CT RC12 Benchmark Framework*",
        ]

        return '\n'.join(lines)

    def _summary_table(self, ag: dict) -> str:
        rows = [
            "| Metric | Value |",
            "|--------|-------|",
            f"| Success Rate | {ag.get('success_rate', 0):.1%} |",
            f"| Repair Rate | {ag.get('repair_rate', 0):.1%} |",
            f"| Diff Generation Rate | {ag.get('diff_rate', 0):.1%} |",
            f"| Security Block Rate | {ag.get('security_block_rate', 0):.1%} |",
            f"| Avg Runtime | {ag.get('avg_runtime_ms', 0):.0f}ms |",
            f"| Avg Trust | {ag.get('avg_trust', 0):.2f} |",
            f"| Avg Risk Score | {ag.get('avg_risk_score', 0):.2f} |",
            f"| Rules Used | {ag.get('rules_used', 0)} cases |",
            f"| Learning Active | {ag.get('learning_active', 0)} cases |",
            f"| Total Cases | {ag.get('total', 0)} |",
            f"| Successful | {ag.get('successes', 0)} |",
            f"| Security Blocked | {ag.get('security_blocked', 0)} |",
        ]
        return '\n'.join(rows)

    def _repair_section(self, ag: dict) -> str:
        total = ag.get('total', 1)
        repair = ag.get('repair_successes', 0)
        rate = repair / max(1, total)
        bar = self._bar(rate, 40)

        return '\n'.join([
            f"Repair Success Rate: **{rate:.1%}**",
            f"```",
            f"{bar} {repair}/{total}",
            f"```",
            f"Avg Diff Size: {ag.get('avg_diff_bytes', 0):.0f} bytes",
            f"Diffs Generated: {ag.get('diffs_generated', 0)}/{total}",
        ])

    def _security_section(self, ag: dict) -> str:
        blocked = ag.get('security_blocked', 0)
        total = ag.get('total', 1)

        return '\n'.join([
            f"Security Blocks: **{blocked}/{total}** ({ag.get('security_block_rate', 0):.1%})",
            f"Avg Risk Score: {ag.get('avg_risk_score', 0):.2f}",
            "",
            "SecurityBrain aktiv: scannt jeden Bug VOR Engine-Aufruf.",
            "  - Trojan Source (Unicode Bidi)",
            "  - AST Security (eval/exec/pickle/importlib)",
            "  - Blockiert kritische Funde vor LLM-Execution",
        ])

    def _performance_section(self, ag: dict) -> str:
        return '\n'.join([
            f"Avg Runtime: **{ag.get('avg_runtime_ms', 0):.0f}ms** pro Case",
            f"Total Duration: {ag.get('duration_ms', 0):.0f}ms",
            f"Engine: {ag.get('engine', 'auto')}",
            "",
            "Token-Usage grob geschätzt aus Diff-Größe × 3.",
        ])

    def _learning_section(self, ag: dict) -> str:
        learning = ag.get('learning_active', 0)
        total = ag.get('total', 1)
        rules = ag.get('rules_used', 0)

        return '\n'.join([
            f"BLM Active: **{learning}/{total}** cases",
            f"Consolidator Rules Used: **{rules}/{total}** cases",
            f"Avg Engine Trust: {ag.get('avg_trust', 0):.2f}",
            "",
            "Learning Stack:",
            "  - BugLearningMemory: speichert Erfahrungen",
            "  - ExperienceConsolidator: BLM → PREFER/AVOID Rules",
            "  - BugTypeTrust: Engine-Trust pro Bug-Type",
            "  - FeedbackDampener: verhindert Overconfidence",
            "  - WorkingMemory: Session-Kontext pro Run",
        ])

    def _bar(self, value: float, width: int = 20) -> str:
        """ASCII progress bar."""
        filled = int(value * width)
        return '█' * filled + '░' * (width - filled)

    def generate_json(self, aggregate: dict, pretty: bool = True) -> str:
        """Generate JSON report."""
        import json
        return json.dumps(aggregate, indent=2 if pretty else None)


# ─── Self-Tests ─────────────────────────────────────────────────────────

if __name__ == '__main__':
    gen = ReportGenerator()
    passed = 0

    sample_ag = {
        'total': 100, 'successes': 78, 'success_rate': 0.78,
        'repair_successes': 82, 'repair_rate': 0.82,
        'security_blocked': 12, 'security_block_rate': 0.12,
        'diffs_generated': 90, 'diff_rate': 0.90,
        'avg_runtime_ms': 4200, 'avg_risk_score': 0.05,
        'avg_trust': 0.73, 'avg_diff_bytes': 287,
        'rules_used': 45, 'learning_active': 82,
        'dataset': 'ct_repair_100.jsonl', 'engine': 'opencode',
        'duration_ms': 420000,
    }

    # T1: Markdown generation
    print("T1: Markdown report...", end=" ")
    md = gen.generate_markdown(sample_ag)
    assert '# CT RC12' in md
    assert '78.0%' in md
    assert 'Repair' in md
    assert 'Security' in md
    passed += 1
    print("OK")

    # T2: JSON generation
    print("T2: JSON report...", end=" ")
    js = gen.generate_json(sample_ag)
    assert '"success_rate"' in js
    assert '0.78' in js
    passed += 1
    print("OK")

    # T3: Empty results
    print("T3: Empty results...", end=" ")
    md_empty = gen.generate_markdown({'total': 0})
    assert 'No cases executed' in md_empty
    passed += 1
    print("OK")

    # T4: Bar rendering
    print("T4: Bar...", end=" ")
    bar = gen._bar(0.78, 10)
    assert len(bar) == 10
    assert '█' in bar
    passed += 1
    print("OK")

    print(f"\n{'='*50}")
    print(f"  {passed}/4 Tests bestanden")
    print(f"  {'✅ REPORT GENERATOR FERTIG' if passed >= 4 else '❌'}")
